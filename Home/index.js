    const menu = document.querySelector(".hamburger-menu");
  
    if (toggle && menu) {
  
      toggle.addEventListener("click", (e) => {
        e.stopPropagation();
        menu.style.display =
          menu.style.display === "block" ? "none" : "block";
      });
  
      document.addEventListener("click", (e) => {
        if (!menu.contains(e.target) && !toggle.contains(e.target)) {
          menu.style.display = "none";
        }
      });
  
    }
  
  
    /* ===== FILTER CARDS ===== */
    const filters = document.querySelectorAll(".hamburger-menu li");
    const cards = document.querySelectorAll(".card");
  
    filters.forEach(filter => {
      filter.addEventListener("click", () => {
  
        const category = filter.dataset.filter;
  
        cards.forEach(card => {
  
          if (category === "all") {
            card.style.display = "block";
            return;
          }
  
          const cardCategories = card.dataset.category;
  
          if (cardCategories && cardCategories.includes(category)) {
            card.style.display = "block";
          } else {
            card.style.display = "none";
          }
  
        });
  
      });
    });
  
  
    document.querySelectorAll(".card").forEach(card => {

        const text = card.querySelector(".card-description");
        const btn = card.querySelector(".see-more");
      
        if (!text || !btn) return;
      
        // 👇 esperar render real
        setTimeout(() => {
      
          const isClamped = text.scrollHeight > text.clientHeight + 5;
      
          if (!isClamped) {
            btn.style.display = "none";
          }
      
        }, 100);
      
        btn.addEventListener("click", () => {
      
          const expanded = text.classList.toggle("expanded");
      
          btn.textContent = expanded ? "See less" : "See more";
      
        });
      
      });

